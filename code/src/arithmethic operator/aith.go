package main

import "fmt"

func main() {

	no1 := 2
	no2 := 2

	res1 := no1 + no2
	fmt.Printf("Addition of two number %d\n", res1)

	res2 := no1 - no2
	fmt.Printf("Substraction of two number %d\n", res2)

	res3 := no1 * no2
	fmt.Printf("Multiplication of two number %d\n", res3)

	res4 := no1 / no2
	fmt.Printf("division of two number %d\n", res4)

	res5 := no1 % no2
	fmt.Printf("modulus of two number %d\n", res5)
}
